package com.example.tourism_app;

public class storedata {
    String username,output;
    public storedata(){}

    public storedata(String username, String output) {
        this.username = username;
        this.output = output;
    }

    public String getUsername() {
        return username;
    }

    public String getOutput() {
        return output;
    }
}
